/**
 * Respond with the correct character, given the line of the
 * poem, if this were said at the front door.
 *
 * @param {string} line
 * @returns {string}
 */
export function frontDoorResponse(line) {
  return line.trim()[0];
}

/**
 * Format the password for the front-door, given the response
 * letters.
 *
 * @param {string} word the letters you responded with before
 * @returns {string} the front door password
 */
export function frontDoorPassword(word) {
  let lowerCaseWord = word.toLowerCase();
  let char1 = word.charAt(0).toUpperCase();
  return char1 + lowerCaseWord.slice(1);
}

/**
 * Respond with the correct character, given the line of the
 * poem, if this were said at the back door.
 *
 * @param {string} line
 * @returns {string}
 */
export function backDoorResponse(line) {
  let lineTrim = line.trim();
  let lineLength = lineTrim.length;
  return lineTrim[lineLength-1]
}

/**
 * Format the password for the back door, given the response
 * letters.
 *
 * @param {string} word the letters you responded with before
 * @returns {string} the back door password
 */
export function backDoorPassword(word) {
  let lowerCaseWord = word.toLowerCase();
  let capitalLetter = lowerCaseWord.charAt(0).toUpperCase();
  return capitalLetter + lowerCaseWord.slice(1) + ", please"
}
